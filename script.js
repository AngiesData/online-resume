function renderContact(items) {
  const container = document.getElementById("contact-list");

  if (!container) {
    return;
  }

  container.innerHTML = items
    .map(function (item) {
      let content;

      if (item.link) {
        content =
          '<a href="' +
          item.link +
          '" target="_blank" rel="noopener noreferrer">' +
          item.value +
          "</a>";
      } else {
        content = "<span>" + item.value + "</span>";
      }

      return (
        '<div class="contact-item">' +
        '<span class="contact-label">' +
        item.label +
        "</span>" +
        content +
        "</div>"
      );
    })
    .join("");
}


function renderTags(elementId, items) {
  const container = document.getElementById(elementId);

  if (!container) {
    return;
  }

  container.innerHTML = items
    .map(function (item) {
      return '<span class="tag">' + item + "</span>";
    })
    .join("");
}


function renderEntries(elementId, items) {
  const container = document.getElementById(elementId);

  if (!container) {
    return;
  }

  container.innerHTML = items
    .map(function (item) {
      let bullets = "";
      let projectLink = "";
      let dates = item.dates || "";
      let organization = item.organization || "";

      if (item.bullets && item.bullets.length > 0) {
        bullets =
          "<ul>" +
          item.bullets
            .map(function (bullet) {
              return "<li>" + bullet + "</li>";
            })
            .join("") +
          "</ul>";
      }

      if (item.link) {
        projectLink =
          '<p><a class="project-link" href="' +
          item.link +
          '" target="_blank" rel="noopener noreferrer">' +
          (item.linkText || "View project") +
          " →</a></p>";
      }

      return (
        '<article class="entry">' +
        '<div class="entry-date">' +
        dates +
        "</div>" +
        "<div>" +
        "<h3>" +
        item.title +
        "</h3>" +
        '<p class="entry-subtitle">' +
        organization +
        "</p>" +
        bullets +
        projectLink +
        "</div>" +
        "</article>"
      );
    })
    .join("");
}


